void Clock_Set(){
	RCC->CR|=(1<<16);// HSE on
	RCC->PLLCFGR|=(4<<0)|(150<<6)|(0<<16)|(0<<17)|(1<<22)|(7<<24);// M=4,N=150,P=2,PLL source HSE,Q=4.    
	RCC->CR|=(1<<24);// PLL on
	while(((RCC->CR)&(0x02000000))!=0x02000000);
	RCC->CFGR|=(1<<1)|(0<<4);// AHB Prescaler=2 then Clock=...MHz 
	RCC->AHB1ENR=(1<<0)|(1<<1)|(1<<3);// A, B and D clock enable
	RCC->AHB2ENR|=1<<6;
}
